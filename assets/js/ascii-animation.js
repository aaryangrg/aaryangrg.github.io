export function startAsciiAnimation(containerSelector, startIndex = 0) {
  const asciiContainer = document.querySelector(containerSelector);
  if (!asciiContainer) {
    console.error(`ASCII container not found: ${containerSelector}`);
    return;
  }

  let currentIndex = startIndex;
  function updateAsciiArt() {
    asciiContainer.style.opacity = "0.99";
    setTimeout(() => {
      asciiContainer.innerHTML = asciiFrames[currentIndex];
      asciiContainer.style.opacity = "1";
      currentIndex = (currentIndex + 1) % asciiFrames.length;

      // Reset to start index on loop re-start
      if (currentIndex === 0) {
        currentIndex = startIndex;
      }
    }, 500);
  }

  asciiContainer.innerHTML = asciiFrames[currentIndex];
  setInterval(updateAsciiArt, 200);
}
